#Um programa em Python que calcule média aritmética

nota1 = int(input("Digite a primeira nota recebida:"))
nota2= int(input("Digite a segunda nota recebida:"))
nota3 = int(input("Digite a terceira nota recebida:"))
nota4 = int(input("Digite a quarta nota recebida:"))



media= (nota1+nota2+nota3+nota4)/4

print("A média aritmética desta matéria será equivalente a", media, ".")
