
# Faça um programa em Python que receba o valor do lado de um quadrado e calcule e informe o valor do perímetro e a área deste quadrado.

quadrado = input("Digite o valor correspondente ao lado de um quadrado:")
ladoquad = int(quadrado)

perimetro = ladoquad*4
area= ladoquad**2 

print("O perímetro do quadrado informado é equivalente a", perimetro, "e a área do quadrado corresponde a", area, ".")

